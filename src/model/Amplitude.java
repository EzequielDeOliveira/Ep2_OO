package model;

public class Amplitude {
	
	private int amplitudeTensao;
	private int amplitudeCorrente;
	
	Amplitude(){}
	
	public int getAmplitudeTensao() {
		return amplitudeTensao;
	}
	public void setAmplitudeTensao(int amplitudeTensao) {
		this.amplitudeTensao = amplitudeTensao;
	}
	public int getAmplitudeCorrente() {
		return amplitudeCorrente;
	}
	public void setAmplitudeCorrente(int amplitudeCorrente) {
		this.amplitudeCorrente = amplitudeCorrente;
	}
	

}
